@bot.message_handler(content_types=["text"])
def respond_to_message(message):
    user_text = message.text.strip().lower()

    # –ü–æ–¥–¥–µ—Ä–∂–∫–∞ —Ä—É—á–Ω–æ–≥–æ –≤–≤–æ–¥–∞ –∏ –∫–Ω–æ–ø–æ–∫
